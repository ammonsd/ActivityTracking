--------------------------------
-- Schema Creation and Table Setup
-- Order matters: Users first, then tables that depend on it
---------------------------------

-- Ensure the dbo schema exists
CREATE SCHEMA IF NOT EXISTS dbo;

--------------------------------
-- Create dbo.Users table
---------------------------------

-- Drop if exists (no OBJECT_ID in Postgres)
DROP TABLE IF EXISTS dbo."Users" CASCADE;

CREATE TABLE dbo."Users" (
    id BIGSERIAL PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    firstname  VARCHAR(50),
    lastname  VARCHAR(50) NOT NULL,
    company  VARCHAR(100),
    "password" VARCHAR(255) NOT NULL,
    "role" VARCHAR(20) NOT NULL DEFAULT 'USER',
    enabled BOOLEAN NOT NULL DEFAULT true,
    forcepasswordupdate BOOLEAN NOT NULL DEFAULT true,
    created_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_login TIMESTAMP NULL
);

-- Create unique index on username to prevent duplicate usernames
-- This ensures data integrity and provides optimal query performance
-- Note: CONCURRENTLY cannot be used in schema.sql as it runs in a transaction
CREATE UNIQUE INDEX IF NOT EXISTS idx_users_username_unique 
ON dbo."Users" (username);

-----------------------------------
-- Create dbo.DropdownValues table
-----------------------------------

-- Drop if exists (no OBJECT_ID in Postgres)
DROP TABLE IF EXISTS dbo."DropdownValues" CASCADE;

-- Recreate with CamelCase column names preserved
CREATE TABLE dbo."DropdownValues" (
    "RECID"        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "category"     VARCHAR(50)  NOT NULL,
    "value"        VARCHAR(255) NOT NULL,
    "displayOrder" INTEGER      NOT NULL DEFAULT 0,
    "isActive"     BOOLEAN      NOT NULL DEFAULT TRUE,
    CONSTRAINT "UK_DropdownValues_Category_Value" UNIQUE ("category", "value")
);

--------------------------------
-- Create dbo.TaskActivity table
---------------------------------

-- Drop if exists (no OBJECT_ID in Postgres)
DROP TABLE IF EXISTS dbo."TaskActivity" CASCADE;

-- Recreate with CamelCase column names preserved
CREATE TABLE dbo."TaskActivity" (
    "RECID"     BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "taskDate"  DATE NOT NULL,
    "client"    VARCHAR(255) NOT NULL,
    "project"   VARCHAR(255) NOT NULL,
    "phase"     VARCHAR(255) NOT NULL,
    "hours"     NUMERIC(4, 2) NOT NULL,
    "details"   VARCHAR(255) NOT NULL,
    "username"  VARCHAR(50) NOT NULL,
    CONSTRAINT "PK_TaskActivity" PRIMARY KEY ("RECID"),
    CONSTRAINT "UQ_TaskActivity" UNIQUE ("taskDate", "client", "project", "phase", "details")
);

-- Add foreign key constraint from TaskActivity to Users
-- This prevents deletion of users who have task activities
ALTER TABLE dbo."TaskActivity"
ADD CONSTRAINT FK_TaskActivity_Username
FOREIGN KEY ("username") REFERENCES dbo."Users"(username)
ON DELETE RESTRICT
ON UPDATE CASCADE;